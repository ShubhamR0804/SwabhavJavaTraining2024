package com.techlabs.ocp.violation.model;

public class SimpleInterest {

	private FixedDeposit fixedDeposit;
	
	
	
	public SimpleInterest(FixedDeposit fixedDeposit) {
		super();
		this.fixedDeposit = fixedDeposit;
		
	}




	public double calculateSimpleInterest() {
		return (fixedDeposit.getPrincipal()*fixedDeposit.getDuration()*fixedDeposit.getFestival().getInterestRate()) / 100 ;
	}
}
